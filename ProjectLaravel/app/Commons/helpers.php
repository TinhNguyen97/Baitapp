<?php

function remove_special_character($string)
{

  $t = $string;

  $specChars = array(
    ' ' => '-',    '!' => '',    '"' => '',
    '#' => '',    '$' => '',    '%' => '',
    '&' => '',    '\'' => '',   '(' => '',
    ')' => '',    '*' => '',    '+' => '',
    ',' => '',    'â‚¹' => '',    '.' => '',
    '/-' => '',    ':' => '',    ';' => '',
    '<' => '',    '=' => '',    '>' => '',
    '?' => '',    '@' => '',    '[' => '',
    '\\' => '',   ']' => '',    '^' => '',
    '_' => '',    '`' => '',    '{' => '',
    '|' => '',    '}' => '',    '~' => '',
    '-----' => '-',    '----' => '-',    '---' => '-',
    '/' => '',    '--' => '-',   '/_' => '-',

  );

  foreach ($specChars as $k => $v) {
    $t = str_replace($k, $v, $t);
  }

  return $t;
}
